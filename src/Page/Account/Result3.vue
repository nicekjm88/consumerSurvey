      const option = {
        maximumFractionDigits: 4,
      };
      this.returnPv = this.returnPv.toLocaleString("ko-KR", option);
    },
    hideModal() {
      const modal = this.$refs.modal;

      window.addEventListener("click", (e) => {
        e.target !== modal ? modal.classList.remove("isActive") : false;
      });

    },
  },
  components: {
    Navigation,
    FixedBtn,
    Modal
  },
};
