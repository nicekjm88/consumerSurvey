  name: "Result3",
  data() {
    return {
      particles: 200,
      mine: 30,
      leftUnder1: 0,
      leftUnder2: 0,
      leftUnder3: 0,
      rightUnder1: 0,
      rightUnder2: 0,
      rightUnder3: 0,
      leftSum: 0,
      rightSum: 0,
      totalSum: 0,
      returnPv: 60000,
      nextStep: false,
      nextStep2: false,
      isAddClass: false,
      isButtonDisabled: false,
      isHide: false
    };
  },
  updated() {
    this.caculation();
  },
  watch: {
    totalSum (value) {
      if ( value >= 60 ) {
        if ( this.leftSum >= 30 && this.rightSum >= 30 ) {
          this.nextStep = true;
          this.isButtonDisabled = true;
        }
      }
    }
  },
  methods: {
    touchHide() {
      this.isHide = true;
    },
    addClass() {
      setTimeout(() => {
        this.isAddClass = true;
        this.hideModal();
      }, 0.3);
    },
    activate(e) {
      e.target.classList.add("is-active");
    },
    isAddPv() {
      this.mine >= 30 ? 30 : (this.mine += 10);
    },
    isAddPv2(e) {
      this.activate(e);
      this.leftUnder1 >= 30 ? 30 : (this.leftUnder1 += 10);
    },
    isAddPv21(e) {
      this.activate(e);
      this.leftUnder2 >= 30 ? 30 : (this.leftUnder2 += 10);
    },
    isAddPv22(e) {
      this.activate(e);
      this.leftUnder3 >= 30 ? 30 : (this.leftUnder3 += 10);
    },
    isAddPv3(e) {
      this.activate(e);
      this.rightUnder1 >= 30 ? 30 : (this.rightUnder1 += 10);
    },
    isAddPv31(e) {
      this.activate(e);
      this.rightUnder2 >= 30 ? 30 : (this.rightUnder2 += 10);
